package land.arisu.tsubaki.core.extensions

import org.koin.core.context.GlobalContext

/**
 * Returns the injectable as [T], a better way to not repeat
 * [GlobalContext.get] multiple times to the point of being unreadable
 */
inline fun <reified T : Any> GlobalContext.getInjectable(): T = this.get().get()
